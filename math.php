          <h3 class="pt-5 pb-3" id="trigono">Trigonometry</h3>
          <p>
              In Ganitapada 6, Aryabhata gives the area of a triangle as:
              <dl>
                  <dd style="font-style: italic;">
                      tribhujasya phalaśarīraṃ samadalakoṭī bhujārdhasaṃvargaḥ
                  </dd>
              </dl>
              that translates to: "for a triangle, the result of a perpendicular with the half-side is the area."
          </p>
          <p>
              Aryabhata discussed the concept of sine in his work by the name of ardha-jya, which literally means "half-chord". For simplicity, people started calling it jya. When Arabic writers translated his works from Sanskrit into Arabic, they referred it as jiba. However, in Arabic writings, vowels are omitted, and it was abbreviated as jb. Later writers substituted it with jaib, meaning "pocket" or "fold (in a garment)". (In Arabic, jiba is a meaningless word.) Later in the 12th century, when Gherardo of Cremona translated these writings from Arabic into Latin, he replaced the Arabic jaib with its Latin counterpart, sinus, which means "cove" or "bay"; thence comes the English word sine.
          </p>

          <h3 class="pt-5 pb-3">Indeterminate equations</h3>
          <p>
              A problem of great interest to Indian mathematicians since ancient times has been to find integer solutions to Diophantine equations that have the form <var>ax + by = c</var>. (This problem was also studied in ancient Chinese mathematics, and its solution is usually referred to as the Chinese remainder theorem.) This is an example from Bhāskara's commentary on Aryabhatiya:
              <dl>
                  <dd>
                      Find the number which gives 5 as the remainder when divided by 8, 4 as the remainder when divided by 9, and 1 as the remainder when divided by 7.
                  </dd>
              </dl>
              That is, find <var>N = 8x+5 = 9y+4 = 7z+1</var>. It turns out that the smallest value for <var>N</var> is <var>85</var>. In general, diophantine equations, such as this, can be notoriously difficult. They were discussed extensively in ancient Vedic text Sulba Sutras, whose more ancient parts might date to 800 BCE. Aryabhata's method of solving such problems, elaborated by Bhaskara in 621 CE, is called the kuṭṭaka (कुट्टक) method. Kuṭṭaka means "pulverizing" or "breaking into small pieces", and the method involves a recursive algorithm for writing the original factors in smaller numbers. This algorithm became the standard method for solving first-order diophantine equations in Indian mathematics, and initially the whole subject of algebra was called kuṭṭaka-gaṇita or simply kuṭṭaka.
          </p>

